package FirstHand_Nov_13;

public class IfElse {

	public static void main (String args[]) { 
		
		int num1 = 1200;
		int num2 = 500;
		
		if(num1 > num2) {
			System.out.print(num1);
		}else {
			System.out.print(num2);
		}
		
		
	}
}

