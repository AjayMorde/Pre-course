package Practise_Nov_14;

public class ForLoopExamples {
	
	public static void main (String args[]) {
		
		// 500 to 1
		for (int num = 500; num >= 1; num = num-1) {
			System.out.println(num); 
		}
		
		
		//all multiples of 2
		for (int my_num = 2; my_num <= 100; my_num = my_num+2) {
			System.out.println(my_num); 
		}

		
		
	}

}
