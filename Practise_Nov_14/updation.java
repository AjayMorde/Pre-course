package Practise_Nov_14;

public class updation {
	public static void main (String args[]) {
		
		int num = 100;
		
		num = num + 10; // num += 10;
		System.out.println(num); // 110
		
		
		num = num - 30; // num -= 30;
		System.out.println(num); // 80
		
		
		num = num * 5; // num *= 5;
		System.out.println(num); // 400
		
		num = num/10;  // num /= 10;
		System.out.println(num); //40
		
		
	}
}
