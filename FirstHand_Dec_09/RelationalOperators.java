package FirstHand_Dec_09;

public class RelationalOperators {
 public static void main (String args[]) {

	 System.out.println(100 == 100); // T
	 System.out.println(100 == 10); // F
	 System.out.println(1 == 1000); // F
	 
	 
	 System.out.println(100 != 100); // F
	 System.out.println(100 != 10); // T
	 System.out.println(1 != 1000); // T
	 
	 
	 System.out.println(100 > 100); // F 
	 System.out.println(100 > 10); // T
	 System.out.println(1 > 1000); // F
	 
	 
	 System.out.println(100 < 100); // F 
	 System.out.println(100 < 10); // F
	 System.out.println(1 < 1000); // T
	 
	 System.out.println(100 >= 100); // T 
	 System.out.println(100 >= 10); // T
	 System.out.println(1 >= 1000); // F
	 
	 
	 System.out.println(100 <= 100); // T 
	 System.out.println(100 <= 10); // F
	 System.out.println(1 <= 1000); // T
	 
 }
}
