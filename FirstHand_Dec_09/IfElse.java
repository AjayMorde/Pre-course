package FirstHand_Dec_09;

public class IfElse {
	public static void main (String args[]) {
		int num1 = 300;
		int num2 = 50;
		
		if (num1 > num2) {
			System.out.print(num1);
		}else {
			System.out.print(num2);
		}
		
	}
}

