package FirstHand_Jan_04_2022;

public class IfElse {
	public static void main(String[] args) {
		// print the greatest number between
		// two numbers
		
		int num1 = 20;
		int num2 = 500;
		 
		// 20 > 500 -- false
		if(num1 > num2) {
			System.out.print(num1);
		}else {
			System.out.print(num2); //500
		}
		
		
	}
}
