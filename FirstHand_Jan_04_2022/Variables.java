package FirstHand_Jan_04_2022;

public class Variables {

	public static void main(String[] args) {
		
		// type_of_container name_of_container = value;
		byte age = 29;
		short salary = 20000;
		int pincode = 800001;
		long phoneNumber = 987655656567L;
		
		float percentage = 94.2F;
		double randomNum = 346537.32323;
		
		boolean isIndian = true;
		boolean isMarried = false;
		
		char sectionInSchool = 'D';
		
		String message = "Welcome to Geekster everyone!!!!@@@";
		
		
		System.out.println("age="+age);
		System.out.println(salary);
		System.out.println(pincode);
		System.out.println(phoneNumber);
		
		System.out.println(percentage);
		System.out.println(randomNum);
		
		System.out.println(isIndian);
		System.out.println(isMarried);
		System.out.println(sectionInSchool);
		System.out.println(message);

	}

}

