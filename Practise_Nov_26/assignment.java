package Practise_Nov_26;

public class assignment {
	public static void main(String args[]) {

	   int num = 10;
	   num = num + 5;  // num += 5;
	   System.out.println(num); //15
	   
	   num = num - 3; // num -= 3;
	   System.out.println(num); //12
	   
	   
	   num = num*4; // num *=4;
	   System.out.println(num); //48
	   
	   
	   num = num/6; // num /= 6;
	   System.out.println(num); //8
	}
}

