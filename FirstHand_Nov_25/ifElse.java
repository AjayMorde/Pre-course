package FirstHand_Nov_25;

public class ifElse {
	public static void main(String args[]) {
		
		int num1 = 250;
		int num2 = 50;
		
		if (num1 > num2) {
			System.out.print(num1);
		}else {
			System.out.print(num2);
		}
	}
}

