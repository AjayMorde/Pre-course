package FirstHand_Dec_22;

public class IfElse {
	public static void main(String[] args) {
		int num1 = 1000;
		int num2 = 200;
		
		if(num1>num2) { //1000 > 200 --> true
			System.out.print(num1);
		}else {
			System.out.print(num2); // 200
		}
	}
}

