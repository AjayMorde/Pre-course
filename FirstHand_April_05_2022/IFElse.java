public class IFElse {

	public static void main(String[] args) {
		
		// Print the greater number in 2 numbers
		int num1 = 2100;
		int num2 = 500;
		
		if (num1 > num2) { //2100 > 500
			System.out.print(num1);
		}else {
			System.out.print(num2);
		}
		
		
	}
}

