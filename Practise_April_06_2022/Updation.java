package April_06_2022;

public class Updation {

	public static void main(String[] args) {
		
		//int num = 20;
		
		int num; // declaration
		num = 20; // assignment / initialisation
		
		// updation 
		num = num + 5;
		System.out.println(num); //25
		
		num = num * 10; //
		System.out.println(num);//250
		
		num = num /25;
		System.out.println(num);//10
		
		num = num - 3;
		System.out.println(num);//7
		
	}
}
