package Practise_Dec_10;

public class updation {
	public static void main(String args[]) {
		
		//int num = 100;
		
		int num; // variable declaration
		num = 100; // initialisation / assignment
		
		num = num + 10; // num += 10;  // updation
		System.out.println(num); //110
		
		num = num - 30; // num -= 30;   // updation
		System.out.println(num); // 80
		
		
		num = num * 5; // num *= 5;
		
		// updation
		System.out.println(num); //400
		
		num = num / 100; // num /= 100; // updation
		System.out.println(num); //4
		
	}
}
