package Practise_Dec_10;

public class OddEven {
	public static void main(String[] args) {
		
		// print all even no.s from 1 to 500;
		
		for (int i = 1; i <= 500; i = i+1) {
			if (i%2 == 0) {
				System.out.println(i);
			}
		}
	}
}

